    def __init__(self):
        self.i       = Signal(16)
        self.i_start = Signal()
        self.i_reset = Signal()

        self.o       = Signal(16)

        self.recip   = FixedPointReciprocal()

    def elaborate(self, platform):
        m = Module()

        m.submodules.recip = self.recip

        with m.FSM():
            with m.State("START"):
                m.d.comb += self.recip.i.eq(self.i)

                with m.If(self.i_start):
                    m.d.sync += self.recip.i_hold.eq(1)
